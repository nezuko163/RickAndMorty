    @Test
    fun getCharactersReturnsSuccessAndCachesData() = runTest {
        val testCharacter = Character(
            id = 1,
            name = "Rick",
            status = "Alive",
            species = "Human",
            type = "",
            gender = "Male",
            origin = LocationInfo("Earth", "url"),
            location = LocationInfo("Earth", "url"),
            image = "image_url",
            episode = listOf("episode1"),
            url = "url",
            created = "date"
        )
        val characterResponse = CharacterResponse(
            info = PageInfo(count = 1, pages = 1, next = null, prev = null),
            results = listOf(testCharacter)
        )

        coEvery { api.getCharactersFlow(any(), any(), any(), any(), any(), any()) } returns
            ResultModel.Success(characterResponse)

        val flow = repository.getCharacters(page = 1)

        assertEquals(true, flow is ResultModel.Success)
        assertEquals(characterResponse, (flow as ResultModel.Success).data)

        val cached = dao.getAllFlow().first()
        assertTrue(cached.isNotEmpty())
        assertEquals(testCharacter.id, cached.first().id)
    }
